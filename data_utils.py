# PartialSpoof Eval Dataset
# Returns: (waveform [66800], frame_labels [208], boundary_labels [208], utt_label, utt_id)
# ============================================================
class Dataset_PartialSpoof_eval(Dataset):
    def __init__(self, list_IDs, seglab, utt_labels, base_dir, skip_missing=False):
        self.list_IDs = list_IDs
        self.seglab = seglab
        self.utt_labels = utt_labels if utt_labels is not None else {}
        self.base_dir = Dataset_PartialSpoof_train._resolve_pt_dir(base_dir, algo=0, is_train=False)
        self.wav_base_dir = os.path.join(os.path.dirname(self.base_dir), 'con_wav')
        self.cut = CUT
        self.skip_missing = skip_missing

    def __len__(self):
        return len(self.list_IDs)

    def __getitem__(self, index):
        utt_id = self.list_IDs[index]
        pt_path = os.path.join(self.base_dir, utt_id + '.pt')
        if os.path.exists(pt_path):
            waveform = torch.load(pt_path, map_location='cpu')
        else:
            wav_path = os.path.join(self.wav_base_dir, utt_id + '.wav')
            if not os.path.exists(wav_path):
                if self.skip_missing:
                    return None
                raise FileNotFoundError(f'Missing both cached tensor and wav for {utt_id}: {pt_path} | {wav_path}')
            x = _load_audio(wav_path, sr=SR)
            x_pad = pad(np.asarray(x, dtype=np.float32), self.cut)
            waveform = torch.tensor(x_pad, dtype=torch.float32)
            try:
                os.makedirs(self.base_dir, exist_ok=True)
                torch.save(waveform.contiguous(), pt_path)
            except Exception:
                pass
        if waveform.dim() != 1:
            waveform = waveform.reshape(-1)
        waveform = waveform[:self.cut].float().contiguous()
        if waveform.numel() != self.cut:
            raise ValueError(f'Cached tensor has wrong length for {utt_id}: {waveform.numel()} != {self.cut}')

        raw_lab = self.seglab.get(utt_id, np.ones(NUM_FRAMES, dtype=str))
        frame_labels, boundary_labels = seglab_to_frame_labels(
            raw_lab, num_frames=NUM_FRAMES
        )
        utt_label = torch.tensor(self.utt_labels.get(utt_id, int(frame_labels.max().item() > 0)), dtype=torch.long)
        return waveform, frame_labels, boundary_labels, utt_label, utt_id


class Dataset_PartialSpoof_multiclass_train(Dataset):
    def __init__(self, list_IDs, frame_label_dict, utt_labels, base_dir, args, algo):
        self.list_IDs = list_IDs
        self.frame_label_dict = frame_label_dict
        self.utt_labels = utt_labels if utt_labels is not None else {}
        self.base_dir = Dataset_PartialSpoof_train._resolve_pt_dir(base_dir, algo=algo, is_train=True)
        self.args = args
        self.algo = algo
        self.cut = CUT

    def __len__(self):
        return len(self.list_IDs)

    def __getitem__(self, index):
        utt_id = self.list_IDs[index]
        pt_path = os.path.join(self.base_dir, utt_id + '.pt')
        waveform = torch.load(pt_path, map_location='cpu')
        if waveform.dim() != 1:
            waveform = waveform.reshape(-1)
        waveform = waveform[:self.cut].float().contiguous()
        if waveform.numel() != self.cut:
            raise ValueError(f'Cached tensor has wrong length for {utt_id}: {waveform.numel()} != {self.cut}')

        raw_lab = self.frame_label_dict.get(utt_id, np.zeros(NUM_FRAMES, dtype=np.int64))
        if getattr(self.args, 'cross_segment_mix_prob', 0.0) > 0 and len(self.list_IDs) > 1:
            donor_index = np.random.randint(0, len(self.list_IDs) - 1)
            if donor_index >= index:
                donor_index += 1
            donor_id = self.list_IDs[donor_index]
            donor_pt_path = os.path.join(self.base_dir, donor_id + '.pt')
            donor_waveform = torch.load(donor_pt_path, map_location='cpu')
            if donor_waveform.dim() != 1:
                donor_waveform = donor_waveform.reshape(-1)
            donor_waveform = donor_waveform[:self.cut].float().contiguous()
            donor_raw_lab = self.frame_label_dict.get(donor_id, np.zeros(NUM_FRAMES, dtype=np.int64))
            waveform, raw_lab = apply_cross_segment_mix(
                waveform,
                raw_lab,
                donor_waveform,
                donor_raw_lab,
                mix_prob=getattr(self.args, 'cross_segment_mix_prob', 0.0),
                min_frames=getattr(self.args, 'cross_segment_mix_min_frames', 8),
                max_frames=getattr(self.args, 'cross_segment_mix_max_frames', 64),
            )
        frame_labels, boundary_labels = multiclass_frame_labels_to_tensors(raw_lab, num_frames=NUM_FRAMES)
        segment_pos_labels = build_segment_position_labels(raw_lab, num_frames=NUM_FRAMES)
        utt_label = torch.tensor(self.utt_labels.get(utt_id, int(frame_labels.max().item() > 0)), dtype=torch.long)
        return waveform, frame_labels, boundary_labels, utt_label, segment_pos_labels


